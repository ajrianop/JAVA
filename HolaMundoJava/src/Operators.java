class Operators {
    public static void main(String[] args) {
        int num1 = 25;
        int num2 = 36;

        //addition
        System.out.println(num1 + num2);

        // Substraction
        System.out.println(num1 - num2);

        // Multiplication
        System.out.println(num1 * num2);

        // Division
        System.out.println(num1 / num2);
        
        // Remainder
        System.out.println(num1 % num2);

        // Example of assignment operators
        // Sum 5 to the num1
        num1 += 5;
        System.out.println(num1);

        // Substraction 5 to the num1
        num1 -= 5;
        System.out.println(num1);
    }
}
