class Operators {
    public static void main(String[] args) {
        int num1 = 25;
        int num2 = 36;

        //addition
        System.out.println(num1 + num2);

        // Substraction
        System.out.println(num1 - num2);

        // Multiplication
        System.out.println(num1 * num2);

        // Division
        System.out.println(num1 / num2);
        
        // Remainder
        System.out.println(num1 % num2);
    }
}
