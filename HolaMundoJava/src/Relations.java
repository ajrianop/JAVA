public class Relations {
    public static void main(String[] args){
        int num1 = 10;
        int num2 = 29;

        // is equal
        System.out.println(num1 == num2);

        // is not equal
        System.out.println(num1 != num2);
        
        // is greater than
        System.out.println(num1 > num2);
        
        // is less than
        System.out.println(num1 < num2);
        
        // is greater or equal than
        System.out.println(num1 >= num2);
        
        // is less or equal than
        System.out.println(num1 <= num2);
    }
}
